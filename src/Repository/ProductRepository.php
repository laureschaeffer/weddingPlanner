    //liste des produits d'une collection
    public function paginateProduct(int $id, int $page): PaginationInterface
    {
        return $this->paginator->paginate(
            $this->createQueryBuilder('p')
                ->where('p.batch = :id')
                ->setParameter('id', $id)
            ,
            $page,
            20
